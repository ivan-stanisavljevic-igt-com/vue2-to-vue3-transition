    trnMakeBrandDictionaryUrl (lang, brand) {
      let mediaServerTranslation = window.ctsautoconf.mediaServerTranslation
      let mediaServer = mediaServerTranslation ? Url.methods.mediaServerHostname() : ''
      let url = mediaServer + '/static/translations/' + brand + '/' + lang + '.json'
      return url
    },

    /**
     * Merge two dictionaries (key-value objects with translations) into one
     * @param {object|null} baseDictionary - dictionary (JS Object) with base/default phrases.
     * @param {object|null} overridingDictionary -  dictionary (JS Object) with phrases that will be added or will overwrite existing
     * @returns {object} new dicitonary with applied overrides
     */
    trnMergeDictionaries (baseDictionary, overridingDictionary) {
      if (typeof baseDictionary === 'undefined' || baseDictionary === null) { baseDictionary = {} }
      if (typeof overridingDictionary === 'undefined' || overridingDictionary === null) { overridingDictionary = {} }
      let newDictionary = Object.assign({}, baseDictionary)
      Object.assign(newDictionary, overridingDictionary)
      return newDictionary
    },

    /**
     * Extract all overrides
     * @param {object} baseDictionary - dictionary to be used as base
     * @param {object} overridingDictionary - dictionary which will be checked for changes when applied over baseDictionary
     * @param {('all'|'new'|'changed'|'same')} diffType - what kind of differences to check
     * @returns {object} dicitonary with matched differences
     */
    trnExtractOverrides (baseDictionary, overridingDictionary, diffType = 'all') {
      let overrides = {}
      if (!baseDictionary) { baseDictionary = {} }

      if (overridingDictionary) {
        Object.keys(overridingDictionary).forEach(overKey => {
          // normalize tags for named formatting (convert Ruby style tags to standard tags)
          let overText2 = overridingDictionary[overKey] || ''
          overText2 = overText2.replaceAll('%{', '{')
          switch (diffType) {
            case 'new':
              if (!baseDictionary.hasOwnProperty(overKey)) {
                overrides[overKey] = overText2
              }
              break
            case 'changed':
              if (baseDictionary.hasOwnProperty(overKey) && baseDictionary[overKey] !== overText2) {
                overrides[overKey] = overText2
              }
              break
            case 'all':
              if (!baseDictionary.hasOwnProperty(overKey) || baseDictionary[overKey] !== overText2) {
                overrides[overKey] = overText2
              }
              break
            case 'same':
              if (baseDictionary.hasOwnProperty(overKey) && baseDictionary[overKey] === overText2) {
                overrides[overKey] = overText2
              }
              break
          }
        })
      }
      return overrides
    },

